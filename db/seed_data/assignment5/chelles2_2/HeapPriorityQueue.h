/* This is a .h file with member functions to implement priority queue using heap */

#ifndef _heappriorityqueue_h
#define _heappriorityqueue_h

#include <iostream>
#include <string>
#include "PQEntry.h"
using namespace std;

class HeapPriorityQueue {
public:
    HeapPriorityQueue();
    ~HeapPriorityQueue();
    void changePriority(string value, int newPriority);
    void clear();
    string dequeue();
    void enqueue(string value, int priority);
    bool isEmpty() const;
    string peek() const;
    int peekPriority() const;
    int size() const;
    friend ostream& operator <<(ostream& out, const HeapPriorityQueue& queue);

private:
    //variables
    PQEntry* heap;
    int capacity;
    int numElements;

    //helper functions
    void bubbleUp(int pos);
    void bubbleDown(int pos);
    void doubleCapacity();
};

#endif
